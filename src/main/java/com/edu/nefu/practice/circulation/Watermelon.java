package com.edu.nefu.practice.circulation;

public class Watermelon {
    public static void main(String[] args) {
      int watermelonNumber = 1;
      int i=9;
      while (0<i){
          watermelonNumber=(watermelonNumber+1)*2;
          i--;
      }
        System.out.println(watermelonNumber);
    }
}
